def get_arch(cmssw_version):
    # Passed string must contain a X_Y_Z-like version substring
    match = re.search(r"CMSSW_\d+_\d+_\d+", cmssw_version)
    if not match:
        raise RuntimeError("Invalid CMSSW version {0}".format(cmssw_version))
    version_string = match.group()
    # Passed string may contain a suffix, X_Y_Z_suffix
    match = re.search(r"CMSSW_\d+_\d+_\d+_.*", cmssw_version)
    if match:
        version_string_with_suffix = match.group()
        cmssw_version = version_string_with_suffix
    else:
        version_string_with_suffix = None
        cmssw_version = version_string
    # Find all possible arches
    version_archs = set([])
    version_with_suffix_archs = set([])
    for arch, releases in RELEASES.items():
        for release in releases:
            if release.startswith(version_string):
                version_archs.update([arch])
            if version_string_with_suffix and release.startswith(
                version_string_with_suffix
            ):
                version_with_suffix_archs.update([arch])
    # Convert to sorted lists
    version_archs = list(version_archs)
    version_archs.sort()
    version_with_suffix_archs = list(version_with_suffix_archs)
    version_with_suffix_archs.sort()
    # Return most likely candidate
    if version_with_suffix_archs:
        archs = version_with_suffix_archs
    elif version_archs:
        archs = version_archs
    else:
        raise RuntimeError(
            "Could not find a suitable arch for cmssw version {0}".format(cmssw_version)
        )
    arch = archs[-1]
    if len(archs) > 1:
        logger.warning(
            "Found multiple candidate arches for cmssw_version %s; "
            "using arch %s (other candidates: %s)",
            cmssw_version,
            arch,
            archs,
        )
    return arch
